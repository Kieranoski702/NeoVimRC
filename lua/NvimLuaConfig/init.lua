require("NvimLuaConfig.set")
require("NvimLuaConfig.remap")

local augroup = vim.api.nvim_create_augroup
local ThePrimeagenGroup = augroup('NvimLuaConfigGroup', {})

local autocmd = vim.api.nvim_create_autocmd
local yank_group = augroup('HighlightYank', {})

function R(name)
    require("plenary.reload").reload_module(name)
end

autocmd('TextYankPost', {
    group = yank_group,
    pattern = '*',
    callback = function()
        vim.highlight.on_yank({
            higroup = 'IncSearch',
            timeout = 40,
        })
    end,
})

autocmd({"CursorHold"}, {
buffer = bufnr,
  callback = function()
    local opts = {
      focusable = false,
      close_events = { "BufLeave", "CursorMoved", "InsertEnter", "FocusLost" },
      border = 'rounded',
      source = 'always',
      prefix = ' ',
      scope = 'cursor',
    }
    vim.diagnostic.open_float(nil, opts)
  end})

autocmd({"BufWritePre"}, {
    group = NvimLuaConfigGroup,
    pattern = "*.lua",
    command = [[%s/\s\+$//e]],
})

